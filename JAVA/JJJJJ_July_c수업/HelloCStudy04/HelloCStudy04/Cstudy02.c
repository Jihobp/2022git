	for (int i = 1; i <= 10; i++)
		sum += i;
	printf("1부터 10까지의 합 : %d\n", sum);

	//for문도 중괄호로 묶을 수 있다.
	for (int i = 0; i < 5; i++)
	{
		if (i % 2 == 0) //2의 배수
			printf("*");//*로 출력
		else
			printf("!");//!로 출력
	}

	//2. while 문
	//3. do while 문


