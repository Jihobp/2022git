#include<stdio.h>
int main()
{
	//  1. 1부터 100까지의 합을 구해보세요.

	int sum = 0;
	for (int i = 1; i <= 100; i++)
		sum += i; //sum이란 변수에 i를 더한다. i가 100이하인지 체크, i++에서 i 출력 후 1을 더한다. 
	printf("1부터 100까지의 합은 %d입니다.\n", sum);


	//	2. 1부터 100까지의 합을 구하는 데, 3의 배수에 대해서만 구해보시오.
	sum = 0; //sum을 초기화 해준다.
	for (int i = 1; i <= 100; i++)
	{
		if (i % 3 == 0)
			sum += i;
	}
	printf("1~100까지의 합(3의 배수만) : %d\n", sum);
	//Q. ㄴ> :  ??

	//	3. 1부터 n까지의 곱을 보세요. (n 입력값)
	printf("몇까지 곱할까요(10까지만 적자)\n");
	int n;
	scanf_s("%d", &n);
	sum = 1; //0으로 초기화하면 안된다.
	for (int i = 1; i <= n; i++)
		sum *= i;
	printf("1부터 %d까지의 곱 : %d\n", n, sum);


	//	4. 입력한 값에 따라서 다음과 같은 삼각형을 출력해보세요. ????????????????????????????????????????????????????????????? 
	//	입력 : 3
		//*
		//**
		//***
	int floor; //4~7번 이 변수 쓸거임 (floor문만큼 반복한다.) 
	printf("몇 층?");
	scanf_s("%d", &floor); //5입력하면 5층floor 똳.
	for (int i = 0; i < floor; i++)
	{
		for (int j = 0; j < i + 1; j++)
			printf("*"); //i값이 증가함에 따라 *이증가.
		printf("\n");
	}
	//	5. 3번과 반대되는 삼각형을 만드시오. ????????????????????????????????????????????????????????????? 
		//입력 : 3
		//*
		//**
		//***
		//띄어쓰기랑 별 둘 다 반복문으로 한다.
	for (int i = 0; i < floor; i++)
	{
		for (int j = 0; j < floor - i - 1; j++)
			printf(" ");
		for (int j = 0; j < i + 1; j++)
			printf("*");
		printf("\n");
	}
	//i, j혼동 nono, for 문 세미콜론 nono.


	//	6. 입력값을 이용하여 피라미드를 만들어보세요. ????????????????????????????????????????????????????????????? 
		//입력 : 3
		//*
		//***
		//*****
		//2n+1 법칙 = 1 3 5 7 9 수열을 따름
		//띄어쓰기는 갈수록 줄어듦
	for (int i = 0; i < floor; i++)
	{
		for (int j = 0; j < floor - i - 1; j++)
			printf(" ");
		for (int j = 0; j < 2 * i + 1; j++)
			printf("*");
		printf("\n");
	}

	//	7. 입력값을 이용하여 다이아몬드를 만들어보세요. ????????????????????????????????????????????????????????????? 
		//입력 : 3
		//*
		//***
		//*****
		//***
		//*
	for (int i = 0; i < floor; i++)
	{
		for (int j = 0; j < floor - i - 1; j++)
			printf(" ");
		for (int j = 0; j < 2 * i + 1; j++)
			printf("*");
		printf("\n");
	}
	for (int i = 0; i < floor - 1; i++)
	{
		//띄어쓰기가 점점 증가
		for (int j = 0; j < i + 1; j++)
			printf(" ");
		//별은 점점 감소
		for (int j = 0; j < (2 * (floor - i - 1)) - 1; j++)
			printf("*");
		printf("\n");
	}


		return 0;
	}